<h1>Regulations to prevent the spread of the novel coronavirus</h1>

<p>
  All staff is advised to enforce strict hygiene regulations to prevent the spread of the novel coronavirus.<br>
  These regulations include the wearing of a mask that covers your nose and mouth and
  keeping a safe distance (at least 1,5m) from other guests.<br>
  Please be advised that failing to follow these regulations might result in an ejection from the event space.
</p>